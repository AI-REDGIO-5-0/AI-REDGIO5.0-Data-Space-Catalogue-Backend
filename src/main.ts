import { NestFactory } from '@nestjs/core';
import { AppModule } from './app.module';
import { Logger, ValidationPipe } from '@nestjs/common';
import { ConfigService } from '@nestjs/config';

import { DocumentBuilder, SwaggerModule } from '@nestjs/swagger';
import { MikroORM } from '@mikro-orm/core';
import mikroConfig from '../mikro-orm.config';

async function bootstrap() {
  const app = await NestFactory.create(AppModule, {
    cors: true,
  });
  app.setGlobalPrefix('api');
  app.useGlobalPipes(new ValidationPipe());

  const config = app.get(ConfigService);
  const port = config.get<number>('app.port', 3000);
  const isDevelopment = config.get<boolean>('app.isDevelopment');

  await MikroORM.init(mikroConfig);

  if (isDevelopment) {
    const swaggerBaseUrl = config.get<string>('app.swaggerBaseUrl');
    const swaggerConfig = new DocumentBuilder()
      .setTitle(config.get('app.name'))
      .setVersion('1.0')
      .addBearerAuth()
      .addServer(swaggerBaseUrl)
      .build();
    const document = SwaggerModule.createDocument(app, swaggerConfig);
    SwaggerModule.setup('api', app, document);
  }
  await app.listen(port);
  Logger.log(
    `ðŸš€ Application "${config.get('app.name')}" is running on port ${port}`,
  );
}
bootstrap();
